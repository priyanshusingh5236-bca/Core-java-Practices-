public class Incrementdecrement {
    public static void main(String[] args) {
        int x = 8;
        System.out.println(x);
          x++; // x++ ka matlb hai x=x+1 matlb 
               // x ki current value ko ek badha do.
          System.out.println(x);
          x--; // x-- ka matlb hai x=x-1 matlb
               // x ki current value ko ek minus kr do.
          System.out.println(x);
    }

}
